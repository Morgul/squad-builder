// ---------------------------------------------------------------------------------------------------------------------
// HomeController
//
// @module home.js
// ---------------------------------------------------------------------------------------------------------------------

function HomeController($scope, $http)
{
    $http.get('/news')
        .success(function(news)
        {
            $scope.news = news;
        });
} // end HomeController

// ---------------------------------------------------------------------------------------------------------------------

angular.module('squad-builder').controller('HomeController', [
    '$scope',
    '$http',
    HomeController
]);

